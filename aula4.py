# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero
# Positivo ou negativo. Int sem sinal eh considerado positivo
print(11) #int
print(-11) #int
print(0) #int


# Float -> numero com ponto flutuante
# O tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante
# float sem sinal eh considerado posiitivo
print(1.1, 10.11) #float
print(0.0) #float
print(-1.5) #float

# a função type mostra o tipo que o python
# inferiu o valor.
print(type('Magnum'))
print(type(11))
print(type(1.2), type(-1.2), type(0.0))

