"""
Python = Linguagem de programação
Tipo de linguagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""


# Aspas simples
print('Magnum de Abreu')
print(1, 'Magnum "de Abreu"')

# Aspas duplas
print("Magnum de Abreu")
print(2, "Magnum 'de Abreu'")


# Escape
print("Magnum \"de Abreu\"")


# r
print(r"Magnum \"de Abreu\"")
